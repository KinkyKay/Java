package com.company.JavaCourse.day8;

public class Resident {
    private String nameResident;
    private String streetNameResident;
    private int houseNumberResident;
    private int postalCodeResident;
    private String cityResident;

    public String getNameResident() {
        return nameResident;
    }

    public void setNameResident(String nameResident) {
        this.nameResident = nameResident;
    }

    public String getStreetNameResident() {
        return streetNameResident;
    }

    public void setStreetNameResident(String streetNameResident) {
        this.streetNameResident = streetNameResident;
    }

    public int getHouseNumberResident() {
        return houseNumberResident;
    }

    public void setHouseNumberResident(int houseNumberResident) {
        this.houseNumberResident = houseNumberResident;
    }

    public int getPostalCodeResident() {
        return postalCodeResident;
    }

    public void setPostalCodeResident(int postalCodeResident) {
        this.postalCodeResident = postalCodeResident;
    }

    public String getCityResident() {
        return cityResident;
    }

    public void setCityResident(String cityResident) {
        this.cityResident = cityResident;
    }
}
